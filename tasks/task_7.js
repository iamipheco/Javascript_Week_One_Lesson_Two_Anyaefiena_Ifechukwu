let dayOfWeek = "Monday"; // Declare and assigned a string value representing a day of the week

switch (dayOfWeek) {
  case "Monday":
    console.log("Monday: Start of the week"); // Output: "Monday: Start of the week"
    break;
  case "Tuesday":
    console.log("Tuesday: Second day of the week"); // Output: "Tuesday: Second day of the week"
    break;
  case "Wednesday":
    console.log("Wednesday: Third day of the week"); // Output: "Wednesday: Third day of the week"
    break;
  case "Thursday":
    console.log("Thursday: Fourth day of the week"); // Output: "Thursday: Fourth day of the week"
    break;
  case "Friday":
    console.log("Friday: Fifth day of the week"); // Output: "Friday: Fifth day of the week"
    break;
  case "Saturday":
    console.log("Saturday: Sixth day of the week"); // Output: "Saturday: Sixth day of the week"
    break;
  case "Sunday":
    console.log("Sunday: Seventh day of the week"); // Output: "Sunday: Seventh day of the week"
    break;
  default:
    console.log("Invalid day of the week"); // Output: "Invalid day of the week"
    break;
}
